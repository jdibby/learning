# Python program to print a heart shape

def print_heart():
    heart = [
        "  **      **  ",
        "******* *******",
        "***************",
        " ************* ",
        "   *********   ",
        "     *****     ",
        "      ***      ",
        "       *       "
    ]
    
    for line in heart:
        print(line)
 
print_heart()
